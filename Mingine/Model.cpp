	void Model::draw()
	{
		shader.use();
		FinalGame::getInstance()->applyCommonShaderValues(shader);

		glm::vec3 objColor = glm::vec3(0.3, 0.1, 0.7);

		int objColorLoc = glGetUniformLocation(shader.getProgramId(), "objectColor");
		glUniform3f(objColorLoc, objColor.x, objColor.y, objColor.z);


		glBindVertexArray(VAO);

		glDrawArrays(GL_TRIANGLES, 0, 36);
	}

