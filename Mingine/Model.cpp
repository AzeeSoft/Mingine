	void Model::prepareShader()
	{
		shader.init();
		shader.attachVertexShader("shaders/vertexShader.shader");
		shader.attachFragmentShader("shaders/fragmentShader.shader");

		shader.use();

		glm::mat4 model;
		model = glm::rotate(model, glm::radians(-55.0f), glm::vec3(1.0f, 0.0f, 0.0f));

		glm::mat4 projection;
		projection = glm::perspective(glm::radians(45.0f), 1024.0f / 768.0f, 0.1f, 100.0f);

		int modelLoc = glGetUniformLocation(shader.getProgramId(), "model");
		glUniformMatrix4fv(modelLoc, 1, GL_FALSE, glm::value_ptr(model));

		int projLoc = glGetUniformLocation(shader.getProgramId(), "projection");
		glUniformMatrix4fv(projLoc, 1, GL_FALSE, glm::value_ptr(projection));
	}

